
package veloxization.tikapeiiris;

/**
 *
 * @author Veloxization
 */
public class Annos {
    
    private int id;
    private String nimi;
    
    public Annos(int id, String nimi) {
        this.id = id;
        this.nimi = nimi;
    }
    
    public int getId() {
        return id;
    }
    
    public String getNimi() {
        return nimi;
    }
}
